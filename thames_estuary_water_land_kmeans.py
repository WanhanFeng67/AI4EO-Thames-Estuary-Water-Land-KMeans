def get_sentinel2_composite(year, region):
    """
    Fetch Sentinel-2 Surface Reflectance imagery for summer months
    and create a median composite for the selected year.
    """

    start_date = f'{year}-06-01'
    end_date = f'{year}-09-30'

    collection = ee.ImageCollection('COPERNICUS/S2_SR_HARMONIZED') \
        .filterDate(start_date, end_date) \
        .filterBounds(region) \
        .filter(ee.Filter.lt('CLOUDY_PIXEL_PERCENTAGE', 20))

    print(f'{year} number of images:', collection.size().getInfo())

    image = collection.median().clip(region)

    return image

