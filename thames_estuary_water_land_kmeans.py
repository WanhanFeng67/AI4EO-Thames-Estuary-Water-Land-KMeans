def calculate_area_km2(mask, region):
    """
    Calculate area of a binary mask in square kilometres.
    """

    area_image = ee.Image.pixelArea()

    area = area_image.updateMask(mask).reduceRegion(
        reducer=ee.Reducer.sum(),
        geometry=region,
        scale=10,
        maxPixels=1e9
    )

    area_km2 = ee.Number(area.get('area')).divide(1e6)

    return area_km2

