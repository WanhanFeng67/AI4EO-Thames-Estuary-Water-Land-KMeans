def get_water_mask_kmeans(ndwi, region, year):
    """
    Apply K-means clustering to NDWI image.
    The cluster with higher mean NDWI is interpreted as water.
    """

    # Sample NDWI pixels
    samples = ndwi.sample(
        region=region,
        scale=10,
        numPixels=5000,
        seed=42,
        geometries=False
    )

    # Train K-means with 2 clusters
    clusterer = ee.Clusterer.wekaKMeans(2).train(samples)

    # Apply clusterer to full NDWI image
    clustered = ndwi.cluster(clusterer)

    # Calculate mean NDWI for cluster 0
    mean_cluster_0 = ndwi.updateMask(clustered.eq(0)).reduceRegion(
        reducer=ee.Reducer.mean(),
        geometry=region,
        scale=10,
        maxPixels=1e9
    ).get('NDWI')

    # Calculate mean NDWI for cluster 1
    mean_cluster_1 = ndwi.updateMask(clustered.eq(1)).reduceRegion(
        reducer=ee.Reducer.mean(),
        geometry=region,
        scale=10,
        maxPixels=1e9
    ).get('NDWI')

    mean0 = ee.Number(mean_cluster_0)
    mean1 = ee.Number(mean_cluster_1)

    print(f'{year} mean NDWI cluster 0:', mean0.getInfo())
    print(f'{year} mean NDWI cluster 1:', mean1.getInfo())

    # Higher NDWI cluster is water
    water_cluster = ee.Number(ee.Algorithms.If(mean0.gt(mean1), 0, 1))

    water_mask = clustered.eq(water_cluster)
    land_mask = water_mask.Not()

    return water_mask, land_mask, clustered

